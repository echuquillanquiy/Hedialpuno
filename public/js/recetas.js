//Medicamento 1
$(document).ready(function() {
    $('#obtenerMed1').change(function() {
        var selectElement = document.getElementById('obtenerMed1');
        var code1 = selectElement.options[selectElement.selectedIndex].value;
        var med1 = selectElement.options[selectElement.selectedIndex].text;

        $('#med1').val(med1);
        $('#code1').val(code1);
    });
});

